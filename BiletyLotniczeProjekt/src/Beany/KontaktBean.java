package Beany;

public class KontaktBean 
{
    Integer kontaktID;
    Integer kontaktUzytkownikID;
    String kontaktEmail;
    String kontaktTelefon;

    public Integer getKontaktID() {
        return kontaktID;
    }

    public void setKontaktID(Integer kontaktID) {
        this.kontaktID = kontaktID;
    }

    public Integer getKontaktUzytkownikID() {
        return kontaktUzytkownikID;
    }

    public void setKontaktUzytkownikID(Integer kontaktUzytkownikID) {
        this.kontaktUzytkownikID = kontaktUzytkownikID;
    }

    public String getKontaktEmail() {
        return kontaktEmail;
    }

    public void setKontaktEmail(String kontaktEmail) {
        this.kontaktEmail = kontaktEmail;
    }

    public String getKontaktTelefon() {
        return kontaktTelefon;
    }

    public void setKontaktTelefon(String kontaktTelefon) {
        this.kontaktTelefon = kontaktTelefon;
    }
    
    
}
